# 两个字符串的字面量拼接直接用“+”
print("学IT来黑马" + "月薪过万")
# 字面量和变量的拼接或者变量和变量的拼接
name = "黑马程序员"
print("我的名字是:" + name + "我可以教大家IT技能")
# 输出：我的名字是:黑马程序员我可以教大家IT技能
print("我的名字是:", name, "我可以教大家IT技能")
# 输出：我的名字是: 黑马程序员 我可以教大家IT技能

print("===============================================")
# 字符串拼接只能拼接字符串类型变量，数字类型和其他类型要报错
name2 = "张三"
tel = str(123456789)
print("My name is: " + name2 + ",My phone score is:" + tel)


